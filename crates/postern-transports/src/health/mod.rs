//! 健康事实域：通路死活的只读事实视图与单调状态机（骨架占位，无逻辑）。
//!
//! `Channel` 三件套之一的「健康事实视图」（§3.1 / §3.4）：随通路状态机推进而更新
//! 的事实位（活 / 僵死 / 已关闭），供连接管理层**被动读取**——是事实快照，不是回调，
//! 本域不主动 push 给 daemon。健康事实有两个来源合流：①保活任务判定（心跳超时 /
//! 续约失败 → 死亡）；②底层端点 I/O 信号（EOF / 对端 RST / 桥接泵退出 → 死亡）。
//! 二者任一触发即推进到「死亡」，且为**单调**——一旦死亡绝不自行翻回活（翻回只能是
//! 连接管理重建出的**新** `Channel`，与本视图无关）。健康事实不含任何真实地址 / 凭据。
//!
//! 子模块：
//! - [`state`]：死活事实位与单调推进的状态机内核。

pub mod state;

