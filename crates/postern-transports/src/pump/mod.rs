//! 桥接泵域：本地端点 ⇆ 底层隧道的双向字节搬运（骨架占位，无逻辑）。
//!
//! 三形态（direct/ssh/ssm）在本地 socket 端点与底层隧道之间架一个**双向字节泵**
//! ——一个后台 tokio 任务，两个方向各一条 `read → write` copy 回路（概念等价于
//! `tokio::io::copy_bidirectional` 语义），以**有界缓冲**承接，写慢即对读侧形成
//! 天然背压，绝不无界堆积（§3.2 / §3.6 资源边界）。泵**只搬字节、不解析协议**
//! （不认识字节里是 SQL 还是 HTTP，那是适配器的事），是「对底层形态零感知」在
//! 搬运层的体现。两方向的 EOF / IO 错误即通路死活的一手信号：任一方向 EOF 或
//! RST 即推进该方向收尾，两方向都不可搬运时泵退出并把「死亡」翻进健康视图。
//! 泵退出与 `Channel` 关闭互为收口：关闭 ⇒ 取消泵任务；泵因底层断开退出 ⇒ 死亡。
//!
//! 子模块：
//! - [`bridge`]：双向 copy 回路与有界缓冲背压、EOF/错误收尾、取消收口。

pub mod bridge;

pub use bridge::{spawn_bridge, PumpHandle, BRIDGE_BUFFER_BYTES};
