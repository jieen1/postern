//! 保活域：长连接型通路的心跳 + 协议级续约状态机（骨架占位，无逻辑）。
//!
//! 保活只作用于**本条已建**通路，是绑定在 `Channel` 生命周期上的后台异步任务，
//! 以「活」为初态（§3.3）。两类手段：①**心跳**——对长连接型按固定节律发协议级
//! keepalive 探测，尽早发现僵死；②**协议级续约**——对**有时限**通路（SSM 会话 /
//! 租约）在「临近到期阈值」（`expiry − skew`）触发续约请求，成功则刷新到期点。
//! `expiry` 不是本域常量，而是底层会话 / 租约建立时由远端给出的到期点。
//!
//! 最核心纪律：续约失败 / 心跳判定僵死 → 一次性转「死亡」并写入健康视图，
//! **就地停摆、不自愈、不重连**（§7-3、§3 验收 L-4）——状态机里根本不存在
//! 「失败后重建」的转移边。与机密面会话续会话（03-secrets 6.13）层级不同、互不替代。
//!
//! 子模块：
//! - [`renew`]：心跳节律 + 续约阈值触发 + 死亡终态推进的状态机。
//! - [`clock`]：可注入时钟抽象——测试以注入时钟驱动续约 / 心跳节律（§9）。

pub mod clock;
pub mod renew;

pub use clock::{Clock, FakeClock, Instant};
pub use renew::{Heartbeat, Keepalive, KeepaliveBackend, KeepaliveOutcome, Renewal};
